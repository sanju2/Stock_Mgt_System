    private void sKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sKeyReleased
         try{
            Connection con = DB.connect();
            ResultSet r = con.createStatement().executeQuery("select * from supplier where supid='"+s.getText()+"'");
            
            boolean b = false;
            while(r.next()){
                b = true;
                String id = r.getString("supid");
                supid.setText(id);
                
                String fn = r.getString("supfname");
                fname.setText(fn);
                
                String ln = r.getString("suplname");
                lname.setText(ln);
                
                String nic1 = r.getString("supnic");
                nic.setText(nic1);
                
            }
            if(!b){
                supid.setText("");
                fname.setText("");
                lname.setText("");
                nic.setText("");
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_sKeyReleased

