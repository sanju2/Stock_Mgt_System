     void setAuto(){
        try{
            Connection con = DB.connect();
            ResultSet e = con.createStatement().executeQuery("select * from supplier ");
            int max=0;
            boolean b=false;
            while(e.next()){
                b=true;
                int id = Integer.parseInt(e.getString("supid"));
                if(max<id){
                    max = id;
                }
            }
            if(b){
                supid.setText(max+1+"");
            }
            else{
                supid.setText("1");
            }
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    void setData(){
        try{
            Connection con = DB.connect();
            ResultSet j = con.createStatement().executeQuery("select * from supplier");
           //vector use for j list(collection frame work)
            Vector v = new Vector();
            while(j.next()){
                 String id = j.getString("supid");
                 String fn = j.getString("supfname");
                 String b = id+" "+fn;
                 
                v.add(b);
               
                 
            }
              jList1.setListData(v);
            
        }
        catch (Exception e){
        
        }
    }

