    private void viewKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_viewKeyReleased
        try{
            Connection con = DB.connect();
            ResultSet r = con.createStatement().executeQuery("select * from item where itemid='"+view.getText()+"'");
            
            boolean b = false;
            while(r.next()){
                b = true;
                String id = r.getString("itemid");
                itemid.setText(id);
                
                String name = r.getString("itemname");
                itemname.setText(name);
                
                String ln = r.getString("catname");
                jComboBox1.setSelectedItem(ln);
                
                String nic1 = r.getString("sup");
                sup.setText(nic1);
                
                String nic2 = r.getString("pup");
                pup.setText(nic2);
                
            }
            if(!b){
                itemid.setText("");
                itemname.setText("");
                jComboBox1.setSelectedItem("");
                sup.setText("");
                pup.setText("");
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_viewKeyReleased

